  // Set default language
  let currentLang = "en"
  
  // Function to update all text content based on selected language
  function updateLanguage(lang) {
    // Update active button
    document.querySelectorAll(".lang-btn").forEach((btn) => {
      btn.classList.remove("active")
      if (btn.getAttribute("data-lang") === lang) {
        btn.classList.add("active")
      }
    })
  
    // Update all text elements with data-lang attribute
    document.querySelectorAll("[data-lang]").forEach((element) => {
      const key = element.getAttribute("data-lang")
      if (translations[lang] && translations[lang][key]) {
        element.textContent = translations[lang][key]
      }
    })
  
    // Update html lang attribute
    document.documentElement.lang = lang
  
    // Save language preference
    localStorage.setItem("preferredLanguage", lang)
  
    // Update current language
    currentLang = lang
  }
  
  // Theme toggle functionality
  function toggleTheme() {
    const body = document.body
    const isDarkTheme = body.classList.contains("dark-theme")
  
    if (isDarkTheme) {
      body.classList.remove("dark-theme")
      body.classList.add("light-theme")
      localStorage.setItem("theme", "light")
    } else {
      body.classList.remove("light-theme")
      body.classList.add("dark-theme")
      localStorage.setItem("theme", "dark")
    }
  
    // Add animation class to body for theme transition
    body.classList.add("theme-transitioning")
    setTimeout(() => {
      body.classList.remove("theme-transitioning")
    }, 1000)
  }
  
  // Initialize theme from saved preference or default to dark
  function initializeTheme() {
    const savedTheme = localStorage.getItem("theme")
  
    if (savedTheme === "light") {
      document.body.classList.remove("dark-theme")
      document.body.classList.add("light-theme")
    } else {
      document.body.classList.remove("light-theme")
      document.body.classList.add("dark-theme")
    }
  }
  
  // Gallery functionality
  function initGallery() {
    const thumbs = document.querySelectorAll(".thumb")
    const mainImage = document.getElementById("main-image")
  
    if (!mainImage) return
  
    thumbs.forEach((thumb) => {
      thumb.addEventListener("click", () => {
        // Update active class
        thumbs.forEach((t) => t.classList.remove("active"))
        thumb.classList.add("active")
  
        // Update main image with smooth transition
        const imgSrc = thumb.getAttribute("data-img")
  
        // Add fade effect
        mainImage.style.opacity = "0"
        mainImage.style.transform = "scale(0.95)"
  
        setTimeout(() => {
          mainImage.src = imgSrc
          mainImage.style.opacity = "1"
          mainImage.style.transform = "scale(1)"
        }, 300)
      })
    })
  }
  
  // Scroll animation function
  function handleScrollAnimations() {
    const animateElements = document.querySelectorAll(
      ".section-header, .feature-card, .download-card, .discord-support, .gallery-container, .video-container, .video-player, .video-description",
    )
  
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("animate")
  
            // If it's a feature card or download card, add staggered animation
            if (entry.target.classList.contains("feature-card") || entry.target.classList.contains("download-card")) {
              const index = Array.from(entry.target.parentNode.children).indexOf(entry.target)
              entry.target.style.transitionDelay = `${index * 0.1}s`
            }
          }
        })
      },
      {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px",
      },
    )
  
    animateElements.forEach((element) => {
      observer.observe(element)
    })
  }
  
  // Parallax effect for hero section
  function initParallax() {
    const heroImage = document.querySelector(".hero-image img")
    const heroContent = document.querySelector(".hero-content")
  
    if (!heroImage || !heroContent) return
  
    window.addEventListener("mousemove", (e) => {
      const x = e.clientX / window.innerWidth
      const y = e.clientY / window.innerHeight
  
      heroImage.style.transform = `translate(${x * 20 - 10}px, ${y * 20 - 10}px) scale(1.05)`
      heroContent.style.transform = `translate(${x * -10 + 5}px, ${y * -10 + 5}px)`
    })
  }
  
  // Animate logo on hover
  function initLogoAnimation() {
    const logo = document.querySelector(".logo-text")
  
    if (!logo) return
  
    logo.addEventListener("mouseenter", () => {
      logo.style.transform = "scale(1.1)"
      setTimeout(() => {
        logo.style.transform = "scale(1)"
      }, 300)
    })
  }
  
  // Button hover effects
  function initButtonEffects() {
    const buttons = document.querySelectorAll(".btn")
  
    buttons.forEach((btn) => {
      btn.addEventListener("mouseenter", () => {
        btn.style.transform = "translateY(-3px) scale(1.02)"
      })
  
      btn.addEventListener("mouseleave", () => {
        btn.style.transform = "translateY(0) scale(1)"
      })
    })
  }
  
  // Initialize language and theme when DOM is loaded
  document.addEventListener("DOMContentLoaded", () => {
    // Initialize theme
    initializeTheme()
  
    // Initialize language
    const savedLang = localStorage.getItem("preferredLanguage")
    if (savedLang && translations[savedLang]) {
      updateLanguage(savedLang)
    } else {
      updateLanguage("en")
    }
  
    // Add event listeners to language buttons
    document.querySelectorAll(".lang-btn").forEach((btn) => {
      btn.addEventListener("click", () => {
        const lang = btn.getAttribute("data-lang")
        updateLanguage(lang)
      })
    })
  
    // Add event listener to theme toggle button
    const themeToggleBtn = document.getElementById("theme-toggle")
    if (themeToggleBtn) {
      themeToggleBtn.addEventListener("click", toggleTheme)
    }
  
    // Initialize gallery
    initGallery()
  
    // Initialize scroll animations
    handleScrollAnimations()
  
    // Initialize parallax effect
    initParallax()
  
    // Initialize logo animation
    initLogoAnimation()
  
    // Initialize button effects
    initButtonEffects()
  
    // Smooth scrolling for anchor links
    document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
      anchor.addEventListener("click", function (e) {
        e.preventDefault()
        const targetId = this.getAttribute("href")
        if (targetId === "#") return
  
        const targetElement = document.querySelector(targetId)
        if (targetElement) {
          window.scrollTo({
            top: targetElement.offsetTop - 80,
            behavior: "smooth",
          })
        }
      })
    })
  
    // Add animation classes to elements that should be animated on page load
    setTimeout(() => {
      document
        .querySelectorAll(
          ".section-header, .feature-card, .download-card, .discord-support, .gallery-container, .video-container, .video-player, .video-description",
        )
        .forEach((el) => {
          el.classList.add("animate")
        })
    }, 300)
  })
  
